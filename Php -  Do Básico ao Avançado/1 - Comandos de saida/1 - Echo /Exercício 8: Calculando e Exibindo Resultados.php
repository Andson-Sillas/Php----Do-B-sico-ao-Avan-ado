<?php

/*
Crie duas variáveis, $a e $b, com valores numéricos. 
Calcule a soma, subtração, multiplicação e divisão e 
exiba os resultados usando echo.
*/


$a = 10;
$b = 20;

$soma = $a + $b;
$subtracao = $a - $b;
$multiplicacao = $a * $b;
$divisao = $a / $b;

echo "A soma de $a mais $b é $soma";
echo "A subtração de $a mais $b é $subtracao";
echo "A multiplicação de $a mais $b é $multiplicacao";
echo "A divisão de $a mais $b é $divisao";
